package pkg1;


public class Tools {
    public void numbers(int n1){
        for (int i=0;i<=n1;i++){
            System.out.print(i+" ");
        }
        System.out.println();
    }
    public void even(int n1){
        for (int i=0;i<=n1;i++){
            System.out.print(i*2+" ");
        }
        System.out.println();
    }
    public void odd(int n1){
        for (int i=0;i<=n1;i++){
            System.out.print(2*i+1+" ");
        }
        System.out.println();
    }
    public void mul13(int n1){
        for (int i=0;i<=n1;i++){
            System.out.print(i*3+" ");
        }
        System.out.println();
    }
    public void mul14(int n1){
        for (int i=0;i<=n1;i++){
            System.out.print(i*4+" ");
        }
        System.out.println();
    }
    public void mul15(int n1){
        for (int i=0;i<=n1;i++){
            System.out.print(i*5+" ");
        }
        System.out.println();
    }
    
        
    }
        
